function shuffle(a) {
  var j, x, i;
  for (i = a.length - 1; i > 0; i--) {
    j = Math.floor(Math.random() * (i + 1));
    x = a[i];
    a[i] = a[j];
    a[j] = x;
  }
  return a;
}

function getRandomInt(max) {
  return Math.floor(Math.random() * Math.floor(max));
}

/// Get a random set of cards of certain type
/// Types: Money, Property, Action, Building or Random if type is null
const generateCards = (type = null, count = getRandomInt(cards.length)) => {
  if (type == null) {
    return shuffle(cards).slice(0, count);
  }

  if (type === 'property') {
    let pSets = [];
    for (let i = 0; i < count; i++) {
      const pCards = cards.filter(card => card.type === type);
      const r = getRandomInt(pCards.length - 3);
      pSets.push(pCards.slice(r, r + 3));
    }

    return pSets;
  }

  return shuffle(cards.filter(card => card.type === type)).slice(0, count);
};

export default generateCards;
